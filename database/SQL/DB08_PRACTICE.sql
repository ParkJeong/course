SELECT DEPTNO, DNAME, EMPNO, ENAME, SAL
  FROM EMP E
INNER JOIN DEPT D
ON E.DEPTNO = D.DEPTNO
 WHERE SAL >= 2000;

SELECT DEPTNO, DNAME, EMPNO, ENAME, SAL
  FROM EMP E, DEPT D
 WHERE E.DEPTNO = D.DEPTNO
   AND SAL >= 2000;

